/** 获取规则列表 GET /api/rule */

export async function result(params, options) {
  console.log(options);
  const msg = await request('/api/result/list', {
    method: 'GET',
    params: {
      page: params.current,
      pageSize: params.pageSize,
    },
    ...(options || {}),
  });
  return {
    data: msg.data.list,
    // success 请返回 true，
    // 不然 table 会停止解析数据，即使有数据
    success: true,
    // 不传会使用 data 的长度，如果是分页一定要传
    total: msg.data.total,
  };
}
